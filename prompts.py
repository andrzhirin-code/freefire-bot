def build_user_prompt(user):
    gyro_text = ""
    if hasattr(user, 'gyro') and user.gyro and user.gyro.lower() in ["да", "yes", "есть", "использую"]:
        gyro_text = "\n- Гироскоп: используется"

    return f"""
Подбери персональную настройку оттяжки для игрока:

- Телефон: {user.phone}
- ОЗУ: {user.ram}
- Стиль игры: {user.style}
- Основное оружие: {user.weapon}
- Количество пальцев: {user.fingers}{gyro_text}
- Проблема: {user.problem if user.problem else 'нет'}

Выдай полную настройку по шаблону.
Пиши красиво, с эмодзи, без лишнего текста.
Названия параметров — ТОЧНО как в игре.
Строго соблюдай правила настройки.
Дай 4-5 персональных советов.
"""

def build_correction_prompt(user, problem):
    return f"""
Игрок уже получил настройку:
- Телефон: {user.phone}
- Стиль игры: {user.style}
- Основное оружие: {user.weapon}
- Пальцы: {user.fingers}

Он жалуется на: {problem}

Дай ИСПРАВЛЕННУЮ настройку. Напиши что именно изменил и почему.
Формат такой же как в первый раз.
"""
